#nesse código iremos demonstrar como variaveis podem ser manipuladas com o uso de operadores aritméticos
a = 5
b = 4

soma = a + b
subtracao= a - b
multiplicacao = a * b
divisao = a / b
divisao_inteira = a//b
modulo = a%b
exponencial = a**b
print("Soma de a+b =", soma)
print("Subtração de a-b =", subtracao)
print("Multiplicação de a*b =", multiplicacao)
print("Divisão de a/b =", divisao)
print("Divisão inteira de a//b =", divisao_inteira)#retorna somente a parte não decimal do resultado
print("Modulo de a%b =", modulo)
print("Exponenciação de a**b", exponencial)